let firstValue = 20
let secondValue = 30

if ( firstValue < secondValue ) {
    console.log(`${firstValue} is less than ${secondValue}`)
}

if(firstValue <= secondValue){
    console.log(`${firstValue} is less than or equal to ${secondValue}`)
}

if(firstValue === secondValue){
    console.log(`${firstValue} exactly equal to ${secondValue}`)
}

if(firstValue !== secondValue){
    console.log(`${firstValue} doesn't equal to ${secondValue}`)
}
